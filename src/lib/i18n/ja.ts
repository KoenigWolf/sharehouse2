/**
 * Deep type that converts all leaf string literals to `string`.
 * This allows en.ts to have different string values while maintaining structure.
 */
type DeepStringify<T> = T extends string
  ? string
  : T extends object
  ? { [K in keyof T]: DeepStringify<T[K]> }
  : T;

/**
 * Translations type for type-safe i18n.
 * Structure is enforced, but string values are flexible for multi-language support.
 */
export type Translations = DeepStringify<typeof ja>;
